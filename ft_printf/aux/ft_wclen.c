#include "aux.h"

int		ft_wclen(wchar_t wc)
{
	int i;

	i = 0;
	if (wc <= 0xFF)
		i = 1;
	else if (wc < 0x0800)
		i = 2;
	else if (wc < 0x010000)
		i = 3;
	else if (wc < 0x110000)
		i = 4;
	return (i);
}
