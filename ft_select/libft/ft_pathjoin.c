#include "libft.h"

char	*ft_pathjoin(char *p1, char *p2)
{
	char	*tmp;

	if (!p1 || !p2)
		return (NULL);
	if (!ft_strendswith(p1, "/"))
	{
		if (p2[0] == '/')
			return (ft_strjoin(p1, p2));
		else
		{
			tmp = ft_strjoincl(ft_strjoinch(p1, '/'), p2, 0);
			return (tmp);
		}
	}
	else
	{
		if (p2[0] == '/')
			return (ft_strjoin(p1, p2 + 1));
		else
			return (ft_strjoin(p1, p2));
	}
}
