#include "aux.h"

void	ft_putnbr(uintmax_t num)
{
	char c;

	if (num < 10)
	{
		c = num + '0';
		ft_putchar(c);
	}
	else
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
}
