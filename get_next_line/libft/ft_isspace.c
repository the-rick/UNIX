#include "libft.h"

int		ft_isspace(int c)
{
	char k;

	k = (unsigned char)c;
	if ((k == '\t') || (k == '\n') || (k == '\v')
			|| (k == '\f') || (k == '\r') || (k == ' '))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
